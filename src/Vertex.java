public class Vertex
 
{
 
	private int xCoord;
	private int yCoord;

	
 
	public Vertex(int x, int y)
	{
		xCoord=x;
		yCoord=y;
 
	}

	public int getXCoord()
 
	{
 
		return xCoord;	

	 }

	public int getYCoord()
 
	{
 
		return yCoord;	

	 }
	
	public void setXCoord(int x)

	{
		xCoord=x;
 
	}
 
	

	public void setYCoord(int y)

	 {
 
		yCoord=y;
 
	}
 
	

}
